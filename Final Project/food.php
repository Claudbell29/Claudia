Snacks: Oranges, Bananas with peanut butter, wheat thins, yogurt and cheese sticks.   
Meals:  Whole Wheat spaghetti, tuna salad (tuna, mayo and celery) wraps, and peanut butter toast with honey. 
Here is a recipe for one of my favorite meals, Healthy Veggie Pizza:
 
<p>•	Whole Wheat Tortilla, Hummus, Peppers, Onions, Meat or Chicken if you want and cheese.</p>


<p>•	To prepare you spread the hummus on the tortilla. Then you add whatever toppings you want and finally you put the cheese on. I normally bake mine in the oven with the temp at 350 for 8-10 minutes.</p>



