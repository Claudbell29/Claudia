Workouts/ Exercise:
I really enjoy doing short, interval treadmill workouts. This is great for getting your heart rate up and burning fat. I love to mix a little cardio with my toning exercises. I normally do 10-15 minutes of alternating sprint and walking every 30 seconds 
Next, I like to do a mix of three arm exercises and three legs. I do three sets of 10 each. Then I will end with some ab exercises.
Leg exercises: Lunges, squats, donkey kicks, wall sits and RDL’s (weighted or body weight)
Arm exercises: Pushups, bench press, DB bent over row, DB curls 
Abs: Planks, weighted sit up, weighted Russian twist, toe touches 

